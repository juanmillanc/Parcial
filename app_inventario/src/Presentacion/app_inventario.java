    private void fnt_consultar(String id) {
        sw = false;
        posicion = 0;

        for (int i = 0; i < implementos.size(); i++) {
            if (id.equals(implementos.get(i).getIdStr())) {
                sw = true;
                posicion = i;
                break;
            }

        }

        if (sw == false) {
            JOptionPane.showMessageDialog(null, "No se encontraron datos");

        } else {
            txt_codigo.setText(implementos.get(posicion).getIdStr());
            txt_implemento.setText(implementos.get(posicion).getImplemantoStr());
            txt_cantidad.setText(implementos.get(posicion).getCantidadStr());
            txt_descripcion.setText(implementos.get(posicion).getDescripcionStr());
        }

    }
    private void btn_consultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_consultarActionPerformed
        fnt_consultar(txt_codigo.getText());
    }//GEN-LAST:event_btn_consultarActionPerformed

