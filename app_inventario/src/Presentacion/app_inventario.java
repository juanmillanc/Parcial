    private void fnt_guardar() {
        List<String> errores = new ArrayList<>();
        if (fnt_validarcodigo(txt_codigo.getText()) == false) {
            errores.add("Codigo");
        }

        if (fnt_validarimplemento(txt_implemento.getText()) == false) {
            errores.add("implemento");
        }
        if (fnt_validarcantidad(txt_cantidad.getText()) == false) {
            errores.add("cantidad");
        }
        if (fnt_validardescripcion(txt_descripcion.getText()) == false) {
            errores.add("descripcion");
        }

        if (errores.isEmpty() == false) {
            String mensajeError = "Se encontraron errores:\n" + String.join("\n", errores);
            JOptionPane.showMessageDialog(null, mensajeError);
            return;
        }
        objValidar.validarDatosEntrada(txt_codigo.getText(), txt_implemento.getText(), txt_cantidad.getText(),
                txt_descripcion.getText());

        if (sw == false) {
            implementos.add(new cls_implementos(txt_codigo.getText(), txt_implemento.getText(), txt_cantidad.getText(),
                txt_descripcion.getText()));
        } else {
            JOptionPane.showMessageDialog(null,
                    "Error, El implementoo con el codigo " + txt_codigo.getText() + " ya existe");

        }

        JOptionPane.showMessageDialog(null, objValidar.getmsn());
    }

    private static boolean fnt_validarcodigo(String codigo) {
        return codigo.matches("\\d+");
    }

    private static boolean fnt_validarimplemento(String implemento) {
        return implemento.matches("^[a-zA-Z]+$");
    }

    private static boolean fnt_validarcantidad(String cantidad) {
        return cantidad.matches("[0-9]{1}|[0-9]{2}");
    }

    private static boolean fnt_validardescripcion(String desccripcion) {
        if (desccripcion == null || desccripcion.trim().isEmpty()) {
            return false;
        } else {
            return desccripcion.matches(".*");

        }
    }

